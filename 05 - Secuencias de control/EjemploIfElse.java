
public class EjemploIfElse {
    public static void main(String[] args) {
    
        int numero = 3;

        if (numero == 1) {
            System.out.println("El número es 1");
        } else if (numero == 2) {
            System.out.println("El número es 2");
        } else if (numero== 3) {
            System.out.println("El número debe ser 3!");
        } else {
            System.out.println("El número es cualquier otra cosa!");
        }
        
    }
}